# A program to input the side of a square and display its perimeter and area.
side = float(input("Enter a side of squre :"))
#Formula of perimeter of squre
peri = 4*side
#Formula of Area of squre
area = side*side
# print perimeter and area of squre
print("Perimeter of squre is : {} \nArea of squre is :{}" .format(peri,area))
